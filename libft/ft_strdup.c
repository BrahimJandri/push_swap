#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	int		len;
	char	*dup;

	i = 0;
	len = ft_strlen(s);
	dup = (char *)malloc(len * sizeof(char) + 1);
	if (!dup)
		return (NULL);
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}

